# Operadores matematicos

primer = input("¿Cuál es el primer número?")
segun = input("¿Cuál es el segundo número?")
# Transformo los string anteriores en valores integer, para hacer asi las operaciones
primero = int(primer)
segundo = int(segun)
# Suma
suma = primero +  segundo
# Resta
resta = primero -  segundo
# Multiplicacion
multiplicacion = primero *  segundo
# Division
division1 = primero /  segundo
# Resto
resto = primero %  segundo
# Division entera
division2 = primero //  segundo

# Imprimo los valores obtenidos por pantalla.
print (" Los valores de las operaciones son los siguientes.")
print ("La suma: " , suma)
print ("La resta: " , resta)
print ("La multiplicación: ", multiplicacion)
print ("La division: ", division1)
print ("El resto: ", resto)
print ("La division entera: ", division2)


